package curso.basico.java.aula01;

public class EstruturaSwichCase {

	public static void main(String[] args) {

		int dias = 13;

		switch (dias) {
		case 1:
			System.out.println("Domigo");
			break;
		case 2:
			System.out.println("Segunda");
			break;
		default:
			System.out.println("erro");
		}
	}

}
